            var privacyAgree = 0;
            
            function submitTrue(){
                if(contactComplete == 1 && privacyAgree == 1){
                   $('button[type="submit"]').prop('disabled', false);
               } else {
                   $('button[type="submit"]').prop('disabled', true);                   
               }
            }
            $('[data-inputmask]').on('keyup', function(){
                var v = $(this).val();
                if(v.slice(-1) == "_"){
                    contactComplete = 0;
                } else {
                    contactComplete = 1;
                }
                submitTrue();
            });
            $('[data-inputmask]').inputmask('+7 (999) 999-99-99', {
                oncleared: function(){ 
                    setTimeout(function(){
                        contactComplete = 0;
                        submitTrue();
                    }, 100);
                }
            });
            $('#privacy').on('input', function(){
                if($('#privacy').is(':checked')) {
                    privacyAgree = 1;
                } else {
                    privacyAgree = 0;
                }
                submitTrue();
            });
            
            $('#messenger').on('input', function(){
                console.log($('#messenger').val().length);
                if($('#messenger').val().length > 3) {
                    contactComplete = 1;
                } else {
                    contactComplete = 0;
                }
                submitTrue();
            });
            
